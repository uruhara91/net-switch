function detectDefault() {
  try {
    const fromStorage = localStorage.getItem(STORAGE_KEY);
    if (fromStorage) return fromStorage;
    const nav = (navigator && navigator.language) ? navigator.language.split('-')[0] : 'en';
    return translations[nav] ? nav : 'en';
  } catch (e) {
    return 'en';
  }
}

let current = detectDefault();

export function getLang() {
  return current;
}

export function setLang(lang) {
  if (!translations[lang]) return;
  current = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
  document.dispatchEvent(new CustomEvent('ns-langchange', { detail: lang }));
}

export function t(key, vars = {}) {
  const dict = translations[current] || translations.en;
  let str = dict[key] || translations.en[key] || key;
  Object.keys(vars).forEach(k => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
  });
  return str;
}

export function onLangChange(cb) {
  document.addEventListener('ns-langchange', (e) => cb(e.detail));
}

export const availableLangs = Object.keys(translations);
